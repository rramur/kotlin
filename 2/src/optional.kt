fun main(args: Array<String>) {

    // To define the variable as optional

    var name1: String?
    name1 = null
    println(name1)

    /*
     * For non optional varaibles assigning the value to NULL is not possible
     * var name2: String
     * name2 = null
     */

    // optional is applicalbe for constants too
    val const_val: Int?
    const_val = null
    println(const_val)







}